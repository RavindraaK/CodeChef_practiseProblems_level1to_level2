import java.util.*;
import java.lang.*;
import java.io.*;

/* Name of the class has to be "Main" only if the class is public. */
class Hoop_Jump
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner read = new Scanner(System.in);
	    int t = read.nextInt();
	    
	    while(t-- > 0)
	    {
	        int n = read.nextInt();
	        
	            System.out.println((n/2) + 1);
	       
	    }
	}
}
